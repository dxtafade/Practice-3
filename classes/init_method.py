class Person:
    def __init__(self, name):
        self.name = name

person = Person("Alex")
print(person.name)


class Car:
    def __init__(self, brand):
        self.brand = brand

car = Car("Toyota")
print(car.brand)


class Book:
    def __init__(self, title):
        self.title = title

book = Book("Python Basics")
print(book.title)


