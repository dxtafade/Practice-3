def add(a, b):
    return a + b

result = add(2, 3)
print(result)


def square(number):
    return number * number

print(square(5))


def full_name(first, last):
    return first + " " + last

print(full_name("John", "Doe"))


def get_max(a, b):
    return max(a, b)

print(get_max(10, 7))


def is_positive(number):
    return number > 0

print(is_positive(-3))